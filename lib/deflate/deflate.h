static int get_distance_code(short distance){
  int lo = 0;
  int hi = 29;
  int mid;

  while (lo <= hi) {
    mid = (lo + hi) >> 1;
    if (DISTANCE_BASE[mid] <= distance) {
      if (mid == 29 || DISTANCE_BASE[mid + 1] > distance)
        return mid;
      lo = mid + 1;
    }else {
      hi = mid - 1;
    }
  }
  return 0;
} 

